from turtle import *

color("red")  # تنظیم رنگ قلم به قرمز
begin_fill()  # شروع رنگ‌آمیزی
pensize(3)    # ضخامت قلم را 3 تنظیم می‌کنیم

left(50)      # چرخش 50 درجه به چپ
forward(133)  # حرکت رو به جلو
circle(50, 200)  # رسم نیم‌دایره برای قسمت چپ قلب
right(140)       # چرخش به راست برای متقارن‌سازی قلب
circle(50, 200)  # رسم نیم‌دایره برای قسمت راست قلب
forward(133)  # حرکت رو به پایین برای بستن قلب

end_fill()  # پایان رنگ‌آمیزی

done()  # پایان اجرای برنامه
